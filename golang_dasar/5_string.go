package main

import "fmt"

func main() {
	fmt.Println("Muhammad")
	fmt.Println("Muhammad Torikal")
	fmt.Println("Muhammad Torikal Akbar")
	
	// funsi len untuk menghitung karakter yang ada pada string
	fmt.Println(len("Muhammad"))
	// Mengambil karakter pada posisi yang di tentukan
	fmt.Println("Muhammad Torikal"[0])
	fmt.Println("Muhammad Torikal Akbar"[1])
}